a = float(input('a = '))
b = float(input('b = '))
print('%d + %d = %d' % (a, b, a+b))
print('%d - %d = %d' % (a, b, a-b))
print('%d * %d = %d' % (a, b, a*b))
print('%f / %f = %f' % (a, b, a/b))   #除法
print('%d // %d = %d' %(a, b, a//b))   #除法取整
print('%d %% %d = %d' % (a, b, a%b))   #除法取余数
print('%d ** %d = %d' % (a, b, a**b))   #幂，a的b次方